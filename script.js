'use strict';

let secretNumber = Math.trunc(Math.random() * 20) + 1;
let score = 20;
let highscore = 0;

const displayMessage = function (message) {
  document.querySelector('.message').textContent = message;
};
const displayScore = function (score) {
  document.querySelector('.score').textContent = score;
};
const styleBackgroundColor = function (color) {
  document.querySelector('body').style.backgroundColor = color;
};
const styleNumberWidth = function (width) {
  document.querySelector('.number').style.width = width;
};

document.querySelector('.check').addEventListener('click', function () {
  const guess = Number(document.querySelector('.guess').value);
  if (!guess) {
    displayMessage('â›”ï¸ No number!');
  } else if (guess === secretNumber) {
    displayMessage('ðŸŽ‰ Correct number!');
    document.querySelector('.number').textContent = secretNumber;
    styleBackgroundColor('#60b347');
    styleNumberWidth('30rem');

    if (score > highscore) {
      highscore = score;
      document.querySelector('.highscore').textContent = highscore;
    }
  } else if (guess !== secretNumber) {
    if (score > 1) {
      displayMessage(
        guess > secretNumber ? 'ðŸ“ˆ Numer is too high!' : 'ðŸ“‰ Number is too low!'
      );
      score--;
      displayScore(score);
    } else {
      displayMessage('ðŸ’¥ You lost the game!');
      displayScore(0);
    }
  }
});

document.querySelector('.again').addEventListener('click', function () {
  score = 20;
  secretNumber = Math.trunc(Math.random() * 20) + 1;
  displayMessage('Start guessing...');
  displayScore(score);
  document.querySelector('.number').textContent = '?';
  document.querySelector('.guess').value = '';
  styleBackgroundColor('#222');
  styleNumberWidth('15rem');
});
